function validateForm() {
    var username = document.getElementById('username').value;
    var password = document.getElementById('password').value;
    // Simple validation rules

    if (username === '' || password === '') {
         alert('Please fill in all fields');
          return false;
}
   
    return true;
}
    
    
    
    
    
    
    
    
    
    
    
    
    


    

    
    
    